package com.starpattern.numbers;

/**
 * Question :
 * 1. print the all stars in 5 format. 
 */

/**
 * @author Satish
 */

public class Pattern1 {

	public static void main(String[] args) {

		for (int i = 1; i <= 5; i++) {

			for (int j = 1; j <= 5; j++) {
				System.out.print("* ");

			}

			System.out.println("");

		}
	}
}
